# Terminal color definitions
class Color():

    BLACK = '\033[30m'
    RED = '\033[31m'
    GREEN = '\033[32m'
    YELLOW = '\033[33m'
    BLUE = '\033[34m'
    MAGENTA = '\033[35m'
    CYAN = '\033[36m'
    WHITE = '\033[37m'
    PURPLE = '\033[38;5;135m'
    BROWN = '\033[38;5;130m'
    ORANGE = '\033[38;5;214m'
    MAROON = '\033[38;5;52m'
    GOLD = '\033[38;5;220m'
    DARKRED = '\033[38;5;88m'
    VIOLET = '\033[38;5;177m'
    CRIMSON = '\033[38;5;160m'
    LIME = '\033[38;5;154m'
    RAINBOW = '\033[38;5;196m'
    RESET = '\033[39m'

    class Style():

        BRIGHT = '\033[1m'
        DIM = '\033[2m'
        NORMAL = '\033[22m'
        RESET_ALL = '\033[0m'
